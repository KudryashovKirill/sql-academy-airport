CREATE TABLE IF NOT EXISTS companies (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS passengers (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100)
);

CREATE TABLE IF NOT EXISTS trips (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company INT NOT NULL,
    plane VARCHAR(255) NOT NULL,
    town_from VARCHAR(255) NOT NULL,
    town_to VARCHAR(255) NOT NULL,
    time_out TIMESTAMP NOT NULL,
    time_in TIMESTAMP NOT NULL,
    FOREIGN KEY (company) REFERENCES companies(id)
);

CREATE TABLE IF NOT EXISTS pass_in_trip (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trip INT NOT NULL,
    passenger INT NOT NULL,
    place VARCHAR(100),
    FOREIGN KEY (trip) REFERENCES trips(id),
    FOREIGN KEY (passenger) REFERENCES passengers(id)
);



